import Foundation

// MARK: - SHCloudHTTPMethod

/// HTTP Methods.
public enum SHCloudHTTPMethod: String {
    
    /// GET Method.
    case get = "GET"
    
    /// POST Method.
    case post = "POST"
    
    /// PATCH Method.
    case patch = "PATCH"
    
    /// PUT Method.
    case put = "PUT"
    
    /// DELETE Method.
    case delete = "DELETE"
}
